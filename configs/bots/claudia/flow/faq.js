import { callUser, getFixed, tagReply } from "./_state.js";

function norm(s = "") { return String(s || "").toLowerCase(); }

export function match(text = "", settings = {}) {
  const t = norm(text);
  return (
    /nome do produto|qual\s*√©\s*o\s*produto|como\s*se\s*chama\s*o\s*produto/.test(t) ||
    /empresa|voc[e√™]s\s*s[√£a]o\s*quem|nome\s*da\s*empresa/.test(t) ||
    /hor[a√°]rio|atendem|funciona\s*at[e√©]\s*quando|que\s*horas\s*voc[e√™]s\s*abrem|fecha/.test(t) ||
    /sorteio|brinde|premi/.test(t) ||
    /garanti|troca|devolu/.test(t) ||
    /aplica|rende|quantas\s*vezes|dura|mes(es)?/.test(t) ||
    /\bml\b|mili|frasco|tamanho/.test(t) ||
    /parcel|divid/.test(t) ||
    /audio|√°udio|mandar\s*voz/.test(t)
  );
}

export default async function faq(ctx) {
  const { text = "", settings, state } = ctx;
  state.turns = (state.turns || 0) + 1;
  const fx = getFixed(settings);
  const t = norm(text);

  if (/nome do produto|qual\s*√©\s*o\s*produto|como\s*se\s*chama\s*o\s*produto/.test(t)) {
    const nm = settings?.product?.name || "Progressiva Vegetal";
    return { reply: tagReply(settings, `O nome do produto √© **${nm}**. Quer que eu te explique rapidinho como ele funciona?`, "flow/faq"), next: "oferta" };
  }
  if (/empresa|voc[e√™]s s[a√£]o quem|nome da empresa/.test(t)) {
    const empresa = settings?.company?.name || fx?.empresa || "TopOfertas";
    return { reply: tagReply(settings, `Somos a **${empresa}** üß°. Posso te ajudar a decidir se combina com teu cabelo?`, "flow/faq"), next: "oferta" };
  }
  if (/hor[a√°]rio|atendem|funciona at[e√©] quando|que horas voc[e√™]s abrem|fecha/.test(t)) {
    const hours = settings?.company?.hours || fx?.hours || "06:00‚Äì21:00";
    return { reply: tagReply(settings, `Atendemos **${hours} (BRT)**, ${callUser(state)}. Quer aproveitar e tirar uma d√∫vida agora?`, "flow/faq"), next: "oferta" };
  }
  if (/sorteio|brinde|premi/.test(t)) {
    const on = settings?.promotions?.raffle?.enabled === true;
    const teaser = on
      ? (settings?.promotions?.raffle?.teaser || "Comprando este m√™s voc√™ participa do nosso sorteio de pr√™mios. Quer que eu te explique rapidinho?")
      : "No momento n√£o temos sorteios ativos, mas te aviso se abrir um novo! ‚ú®";
    return { reply: tagReply(settings, teaser, "flow/faq"), next: "oferta" };
  }
  if (/garanti|troca|devolu/.test(t)) {
    const g = settings?.product?.refund_days_after_delivery ?? 7;
    return { reply: tagReply(settings, `Voc√™ tem **${g} dias** ap√≥s receber. Se n√£o amar, devolvemos sem burocracia. Quer ver o passo a passo de uso?`, "flow/faq"), next: "oferta" };
  }
  if (/aplica|rende|quantas vezes|dura|mes(es)?/.test(t)) {
    return { reply: tagReply(settings, `Rende ${fx.applications} e costuma durar ${fx.duration} (depende dos cuidados). Quer que eu te mande o resumo de aplica√ß√£o?`, "flow/faq"), next: "oferta" };
  }
  if (/\bml\b|mili|frasco|tamanho/.test(t)) {
    const ml = settings?.product?.volume_ml || 500;
    return { reply: tagReply(settings, `O frasco tem **${ml} ml**.`, "flow/faq"), next: "oferta" };
  }
  if (/parcel|divid/.test(t)) {
    return { reply: tagReply(settings, `D√° pra fazer **parcelado** no site, e tamb√©m temos **COD** (paga s√≥ quando recebe). Quer que eu te envie o link seguro?`, "flow/faq"), next: "fechamento" };
  }
  if (/audio|√°udio|mandar\s*voz/.test(t)) {
    return { reply: tagReply(settings, `Se preferir, te mando um **√°udio** com o resumo rapidinho. Quer?`, "flow/faq"), next: "oferta" };
  }

  // fallback neutro
  return { reply: tagReply(settings, `Posso te ajudar com as principais d√∫vidas (uso, prazo, garantia, parcelamento). O que voc√™ prefere saber primeiro?`, "flow/faq"), next: "oferta" };
}
